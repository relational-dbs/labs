-- pais
create table public.pais (
  id bigint primary key not null,
  nombre character varying(100) not null
);
create unique index pais_nombre_key on pais using btree (nombre);
create index pais_nombre_like on pais using btree (nombre);

-- ciudad
create table public.ciudad (
  id bigint primary key not null,
  nombre character varying(100) not null,
  es_capital boolean not null,
  pais_id bigint not null,
  foreign key (pais_id) references public.pais (id)
  match simple on update no action on delete no action
);
create unique index ciudad_nombre_pais_id_uniq on ciudad using btree (nombre, pais_id);
create index ciudad_pais_id on ciudad using btree (pais_id);

-- avion
create table public.avion (
  id bigint primary key not null,
  modelo character varying(100) not null,
  fecha_fabricacion date not null
);

-- pasajero
create table public.pasajero (
  id bigint primary key not null,
  nombre character varying(100) not null,
  apellido character varying(100) not null,
  correo character varying(254) not null,
  telefono character varying(20) not null,
  fecha_nacimiento date not null,
  pais_id bigint not null,
  foreign key (pais_id) references public.pais (id)
  match simple on update no action on delete no action
);
create unique index pasajero_correo_key on pasajero using btree (correo);
create unique index pasajero_telefono_key on pasajero using btree (telefono);
create index pasajero_correo_like on pasajero using btree (correo);
create index pasajero_telefono_like on pasajero using btree (telefono);
create index pasajero_pais_id on pasajero using btree (pais_id);

-- ruta
create table public.ruta (
  id bigint primary key not null,
  millas integer not null,
  avion_id bigint not null,
  ciudad_destino_id bigint not null,
  ciudad_origen_id bigint not null,
  foreign key (avion_id) references public.avion (id)
  match simple on update no action on delete no action,
  foreign key (ciudad_destino_id) references public.ciudad (id)
  match simple on update no action on delete no action,
  foreign key (ciudad_origen_id) references public.ciudad (id)
  match simple on update no action on delete no action
);
create index ruta_avion_id on ruta using btree (avion_id);
create index ruta_ciudad_destino_id on ruta using btree (ciudad_destino_id);
create index ruta_ciudad_origen_id on ruta using btree (ciudad_origen_id);

-- vuelo
create table public.vuelo (
  id bigint primary key not null,
  fecha date not null,
  ruta_id bigint not null,
  foreign key (ruta_id) references public.ruta (id)
  match simple on update no action on delete no action
);
create index vuelo_ruta_id on vuelo using btree (ruta_id);

-- pasajero_vuelo
create table public.pasajero_vuelo (
  id bigint primary key not null,
  no_maletas integer not null,
  asiento character varying(10) not null,
  pasajero_id bigint not null,
  vuelo_id bigint not null,
  foreign key (pasajero_id) references public.pasajero (id)
  match simple on update no action on delete no action,
  foreign key (vuelo_id) references public.vuelo (id)
  match simple on update no action on delete no action
);
create unique index pasajero_vuelo_vuelo_id_asiento_uniq on pasajero_vuelo using btree (vuelo_id, asiento);
create unique index pasajero_vuelo_pasajero_id_vuelo_id_uniq on pasajero_vuelo using btree (pasajero_id, vuelo_id);
create index pasajero_vuelo_pasajero_id on pasajero_vuelo using btree (pasajero_id);
create index pasajero_vuelo_vuelo_id on pasajero_vuelo using btree (vuelo_id);


